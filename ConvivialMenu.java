    private boolean verificaInscritos(){
        int pessoasInscritas = 0;
        for(int i=0;i<listaLocais.size();i++){
            pessoasInscritas += listaLocais.get(i).getListaInscritos().size();
        }
        
        return pessoasInscritas>0;
    }

