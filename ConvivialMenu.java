    private void receitaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receitaButtonActionPerformed

        boolean flag;
        flag = verificaInscritos();
        if(flag){
            Receita novo = new Receita(listaPessoas,listaLocais,nome);
            novo.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null,"Não há pessoas inscritas nos locais!","Erro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_receitaButtonActionPerformed

    private void sairButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sairButtonActionPerformed
        int confirmacao = JOptionPane.showConfirmDialog(null, "Tem a certeza que a pretende terminar sessão?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if(confirmacao == JOptionPane.YES_OPTION){
            this.dispose();
            MainMenu novomenu = new MainMenu(listaPessoas,listaLocais);
            novomenu.setVisible(true);
        }
    }//GEN-LAST:event_sairButtonActionPerformed

